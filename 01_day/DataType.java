public class DataType {
    public static void main(String args[]) {
        // int转为long
        long num1 = 100;
        System.out.println(num1);
        // float转为double
        double num2 = 3.1F;
        System.out.println(num2);
        // long转为float
        float num3 = 10000L;
        System.out.println(num3);
    }
}